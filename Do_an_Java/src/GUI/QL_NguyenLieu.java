    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        try {
            // Kiểm tra txtSoLuongTon
            String soLuongTonStr = txtSoLuongTon.getText().trim();
            double soLuongTon;
            try {
                soLuongTon = Double.parseDouble(soLuongTonStr);
                if (soLuongTon < 0) {
                    JOptionPane.showMessageDialog(null, "Số lượng tồn không được là số âm");
                    txtSoLuongTon.requestFocus();
                    return;
                }
            } catch (NumberFormatException e) {
                JOptionPane.showMessageDialog(null, "Số lượng tồn phải là số hợp lệ");
                txtSoLuongTon.requestFocus();
                return;
            }

            // Nếu bạn có txtSoLuongTP, làm tương tự:
            String soLuongTPStr = txtSoLuongTP.getText().trim();
            double soLuongTP;
            try {
                soLuongTP = Double.parseDouble(soLuongTPStr);
                if (soLuongTP < 0) {
                    JOptionPane.showMessageDialog(null, "Số lượng thành phẩm không được là số âm");
                    txtSoLuongTP.requestFocus();
                    return;
                }
            } catch (NumberFormatException e) {
                JOptionPane.showMessageDialog(null, "Số lượng thành phẩm phải là số hợp lệ");
                txtSoLuongTP.requestFocus();
                return;
            }

            NguyenLieu nl = new NguyenLieu(
                    txtMaNL.getText(),
                    txtTenNL.getText(),
                    cbDVT.getSelectedItem().toString(),
                    soLuongTon // Dùng số đã parse an toàn
            );

            if (dao.insert(nl)) {
                JOptionPane.showMessageDialog(null, "Thêm thành công");
                loadTable();
                clearNguyenLieuForm();
            } else {
                JOptionPane.showMessageDialog(null, "Không thể thêm");
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "Lỗi SQL: " + e.getMessage());
        }
    }//GEN-LAST:event_btnThemActionPerformed

