    private void loadTable() {
        DefaultTableModel model = (DefaultTableModel) tblNguyenLieu.getModel();
        model.setRowCount(0);
        try {
            for (NguyenLieu nl : dao.getAll()) {
                model.addRow(new Object[]{
                    nl.getMaNguyenLieu(), nl.getTenNguyenLieu(), nl.getDVT(), nl.getSoLuongTon()
                });
            }
        } catch (SQLException e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Lỗi khi tải dữ liệu" + e.toString());
        }
    }

    private void loadMaSP() {
        danhSachSanPham = SanPhamDAO.layAllDanhSachSanPham();

        for (SanPham sp : danhSachSanPham) {
            cbMaSP.addItem(sp.getMaSP());
        }

    }

    private void loadTabelCongThuc(String maSP) {
        DefaultTableModel model = (DefaultTableModel) tblCongThuc.getModel();
        model.setRowCount(0);

        try {
            List<CongThuc> ds = ctdao.layCongThucTheoMaSP(maSP);
            List<NguyenLieu> danhSachNguyenLieu = dao.getAll();

            for (CongThuc ct : ds) {
                String tenNL = "";
                for (NguyenLieu nl : danhSachNguyenLieu) {
                    if (ct.getMaNguyenLieu().equals(nl.getMaNguyenLieu())) {
                        tenNL = nl.getTenNguyenLieu();
                        break;
                    }
                }
                model.addRow(new Object[]{
                    ct.getMaNguyenLieu(),
                    tenNL,
                    ct.getSoLuong()
                });
            }
        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Lỗi khi tải dữ liệu: " + e.toString());
        }
    }

