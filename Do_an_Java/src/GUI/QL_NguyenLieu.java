    private void btnThemVaoCongThucActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemVaoCongThucActionPerformed
        DefaultTableModel model = (DefaultTableModel) tblCongThuc.getModel();

        String maNL = txtMaNL.getText().trim();
        String tenNL = txtTenNL.getText().trim();
        String soLuongStr = txtSoLuongTP.getText().trim();

        if (maNL.isEmpty() || tenNL.isEmpty() || soLuongStr.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Vui lòng nhập đầy đủ thông tin nguyên liệu và số lượng!");
            return;
        }

        double soLuong;
        try {
            soLuong = Double.parseDouble(soLuongStr);
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Số lượng phải là số hợp lệ!");
            return;
        }

        boolean daCo = false;
        for (int i = 0; i < model.getRowCount(); i++) {
            String maTrongBang = (String) model.getValueAt(i, 0);
            if (maTrongBang.equals(maNL)) {
                double soLuongCu = (double) model.getValueAt(i, 2);
                model.setValueAt(soLuongCu + soLuong, i, 2); // Cộng dồn
                daCo = true;
                break;
            }
        }

        if (!daCo) {
            model.addRow(new Object[]{maNL, tenNL, soLuong});
        }

        // Xoá ô nhập sau khi thêm
        txtSoLuongTP.setText("");
    }//GEN-LAST:event_btnThemVaoCongThucActionPerformed

