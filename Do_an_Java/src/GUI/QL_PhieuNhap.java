    private void btnchinhsuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnchinhsuaActionPerformed
        int selectedRow = tblNguyenLieu.getSelectedRow();
        if (selectedRow >= 0) {
            // Lấy giá trị từ các trường nhập liệu
            String maNL = cbMaNL.getSelectedItem().toString();
            String tenNL = txtTenNL.getText().trim();
            String dvt = cbDVT.getSelectedItem().toString();
            String soLuongStr = txtSoLuong.getText().trim();
            String donGiaStr = txtDonGia.getText().trim();

            // Kiểm tra dữ liệu nhập vào
            if (tenNL.isEmpty() || soLuongStr.isEmpty() || donGiaStr.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Vui lòng nhập đầy đủ thông tin!");
                return;
            }

            // Kiểm tra số lượng và đơn giá phải là số hợp lệ
            double soLuong, donGia;
            try {
                soLuong = Double.parseDouble(soLuongStr);
                donGia = Double.parseDouble(donGiaStr);
                if (soLuong <= 0 || donGia <= 0) {
                    JOptionPane.showMessageDialog(this, "Số lượng và đơn giá phải là số dương!");
                    return;
                }
            } catch (NumberFormatException e) {
                JOptionPane.showMessageDialog(this, "Số lượng và đơn giá phải là số hợp lệ!");
                return;
            }

            // Tính lại thành tiền
            double thanhTien = soLuong * donGia;

            // Cập nhật lại dòng trong bảng
            DefaultTableModel model = (DefaultTableModel) tblNguyenLieu.getModel();
            model.setValueAt(tenNL, selectedRow, 1); // Cập nhật tên nguyên liệu
            model.setValueAt(dvt, selectedRow, 2); // Cập nhật đơn vị tính
            model.setValueAt(soLuong, selectedRow, 3); // Cập nhật số lượng
            model.setValueAt(donGia, selectedRow, 4); // Cập nhật đơn giá
            model.setValueAt(thanhTien, selectedRow, 5); // Cập nhật thành tiền

            JOptionPane.showMessageDialog(this, "Cập nhật sản phẩm thành công!");
        } else {
            JOptionPane.showMessageDialog(this, "Vui lòng chọn sản phẩm để chỉnh sửa!");
        }
    }//GEN-LAST:event_btnchinhsuaActionPerformed

    private void btnTaoPNActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTaoPNActionPerformed
        try {
            String maPN = txtMaPN.getText().trim();
            String maNV = (String) cbMaNV.getSelectedItem();
            String maNCC = (String) cbNCC.getSelectedItem();

            // Đọc ngày nhập
            String ngayNhapStr = txtNgayNhap.getText().trim();
            Date ngayNhap = null;
            
            try {
                SimpleDateFormat sdf = new SimpleDateFormat("dd/MM/yyyy");
                ngayNhap = sdf.parse(ngayNhapStr);
            } catch (ParseException e) {
                JOptionPane.showMessageDialog(this, "Ngày nhập không hợp lệ! (Định dạng: dd/MM/yyyy)");
                return;
            }

            // Kiểm tra bảng nguyên liệu
            DefaultTableModel model = (DefaultTableModel) tblNguyenLieu.getModel();
            if (model.getRowCount() == 0) {
                JOptionPane.showMessageDialog(this, "Chưa có nguyên liệu nào trong bảng!");
                return;
            }

            // Tính tổng tiền
            double tongTien = 0;
            for (int i = 0; i < model.getRowCount(); i++) {
                tongTien += Double.parseDouble(model.getValueAt(i, 5).toString());
            }

            // Tạo phiếu nhập
            PhieuNhap pn = new PhieuNhap(maPN, maNV,ngayNhap, maNCC, tongTien, "Chưa duyệt");
            
            if (pndao.insertPhieuNhap(pn)) {
                ChiTietPhieuNhapDAO ctpnDAO = new ChiTietPhieuNhapDAO();
                boolean allInserted = true;
                
                for (int i = 0; i < model.getRowCount(); i++) {
                    String maNL = model.getValueAt(i, 0).toString();
                    double soLuong = Double.parseDouble(model.getValueAt(i, 3).toString());
                    double donGia = Double.parseDouble(model.getValueAt(i, 4).toString());
                    double thanhTien = Double.parseDouble(model.getValueAt(i, 5).toString());
                    
                    ChiTietPhieuNhap ctpn = new ChiTietPhieuNhap(maPN, maNL, soLuong, donGia, thanhTien);
                    if (!ctpnDAO.insert(ctpn)) {
                        allInserted = false;
                        break;
                    }
                }
                
                if (allInserted) {
                    JOptionPane.showMessageDialog(this, "Tạo phiếu và chi tiết thành công!");
                    btnTaoPN.setEnabled(false);
                } else {
                    JOptionPane.showMessageDialog(this, "Tạo phiếu thành công nhưng lỗi khi lưu chi tiết!");
                }
            } else {
                JOptionPane.showMessageDialog(this, "Tạo phiếu thất bại!");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e);
        }
    }//GEN-LAST:event_btnTaoPNActionPerformed

