    private void btnThemVaoBangActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemVaoBangActionPerformed
              try {
                if (txtTenNL.getText().trim().isEmpty()
                        || txtSoLuong.getText().trim().isEmpty()
                        || txtDonGia.getText().trim().isEmpty()) {
                    JOptionPane.showMessageDialog(null, "Vui lòng nhập đầy đủ thông tin !!");
                    return;
                }

                String soLuongStr = txtSoLuong.getText().trim();
                String donGiaStr = txtDonGia.getText().trim();

                // Kiểm tra soLuong là số dương
                double soLuongMoi;
                try {
                    soLuongMoi = Double.parseDouble(soLuongStr);
                    if (soLuongMoi <= 0) {
                        JOptionPane.showMessageDialog(null, "Số lượng phải là số dương.");
                        txtSoLuong.requestFocus();
                        return;
                    }
                } catch (NumberFormatException e) {
                    JOptionPane.showMessageDialog(null, "Số lượng phải là số hợp lệ.");
                    txtSoLuong.requestFocus();
                    return;
                }

                // Kiểm tra donGia là số dương
                double donGiaMoi;
                try {
                    donGiaMoi = Double.parseDouble(donGiaStr);
                    if (donGiaMoi <= 0) {
                        JOptionPane.showMessageDialog(null, "Đơn giá phải là số dương.");
                        txtDonGia.requestFocus();
                        return;
                    }
                } catch (NumberFormatException e) {
                    JOptionPane.showMessageDialog(null, "Đơn giá phải là số hợp lệ.");
                    txtDonGia.requestFocus();
                    return;
                }

                String maNL = (String) cbMaNL.getSelectedItem();
                String tenNL = txtTenNL.getText();
                String dvt = (String) cbDVT.getSelectedItem();

                double thanhTienMoi = soLuongMoi * donGiaMoi;

                DefaultTableModel model = (DefaultTableModel) tblNguyenLieu.getModel();
                boolean daTonTai = false;

                for (int i = 0; i < model.getRowCount(); i++) {
                    String maNLDaCo = model.getValueAt(i, 0).toString();
                    if (maNLDaCo.equals(maNL)) {
                        // Cộng dồn số lượng và tính lại thành tiền
                        double slCu = Double.parseDouble(model.getValueAt(i, 3).toString());
                        double dgCu = Double.parseDouble(model.getValueAt(i, 4).toString());

                        double slMoi = slCu + soLuongMoi;
                        double thanhTienMoi2 = slMoi * dgCu;

                        model.setValueAt(slMoi, i, 3); // Số lượng
                        model.setValueAt(thanhTienMoi2, i, 5); // Thành tiền
                        daTonTai = true;
                        break;
                    }
                }

                if (!daTonTai) {
                    model.addRow(new Object[]{maNL, tenNL, dvt, soLuongMoi, donGiaMoi, thanhTienMoi});
                }

                // Tính lại tổng tiền
                double tongTien = 0;
                for (int i = 0; i < model.getRowCount(); i++) {
                    tongTien += Double.parseDouble(model.getValueAt(i, 5).toString());
                }
                txtTongTien.setText(String.format("%.2f", tongTien));

            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, "Lỗi: " + e.toString());
            }
    }//GEN-LAST:event_btnThemVaoBangActionPerformed

