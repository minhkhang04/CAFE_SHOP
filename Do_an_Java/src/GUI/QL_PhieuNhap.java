    private void loadComboBox() {
        try {
            for (NhanVien nv : nvdao.getAll()) {
                cbMaNV.addItem(nv.getMaNV());
            }
            for (NhaCungCap ncc : nccdao.getAll()) {
                cbNCC.addItem(ncc.getMaNCC());
            }
            for (NguyenLieu nl : nldao.getAll()) {
                cbMaNL.addItem(nl.getMaNguyenLieu());
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "Lỗi khi tải dữ liệu: " + e.toString());
        }
    }
    
    private String chuyenDonViTinh(String dvtTrongDB) {
        switch (dvtTrongDB.toLowerCase()) {
            case "gram":
                return "kilogram";
            case "ml":
                return "lít";
            case "cái":
            default:
                return "cái";
        }
    }

