    private String chuyenDonViTinh(String dvtTrongDB) {
        switch (dvtTrongDB.toLowerCase()) {
            case "gram":
                return "kilogram";
            case "ml":
                return "lít";
            case "cái":
            default:
                return "cái";
        }
    }

    private void loadBangCTPN() {
        try {
            List<ChiTietPhieuNhap> ds = ctpndao.getByMaPN(cbMaPN.getSelectedItem().toString());
            DefaultTableModel model = (DefaultTableModel) tblCTPN.getModel();
            model.setRowCount(0);
            String tenNL = "";
            String donVT = "";
            for (ChiTietPhieuNhap ctpn : ds) {
                for (NguyenLieu nl : nldao.getAll()) {
                    if (nl.getMaNguyenLieu().equals(ctpn.getMaNL())) {
                        tenNL = nl.getTenNguyenLieu().toString();
                        donVT = chuyenDonViTinh(nl.getDVT().toString());
                    }
                }
                Object[] row = new Object[]{
                    ctpn.getMaNL(),
                    tenNL,
                    donVT,
                    ctpn.getSoLuong(),
                    String.format("%,.0f", ctpn.getDonGia()), // Định dạng tiền tệ
                    String.format("%,.0f", ctpn.getThanhTien()) // Định dạng tiền tệ
                };
                model.addRow(row);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Lỗi khi tải bảng chi tiết PN: " + e.getMessage());
        }
    }

