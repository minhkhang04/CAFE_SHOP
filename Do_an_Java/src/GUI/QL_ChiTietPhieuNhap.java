    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String maPN = cbMaPN.getSelectedItem().toString();
        String trangThai = cbTrangThai.getSelectedItem().toString();

        // Trường hợp HỦY
        if (trangThai.equals("Hủy")) {
            int confirm = JOptionPane.showConfirmDialog(this,
                    "Bạn có chắc chắn muốn hủy phiếu nhập " + maPN + " không?\nThao tác này sẽ xóa toàn bộ dữ liệu liên quan!",
                    "Xác nhận hủy", JOptionPane.YES_NO_OPTION);

            if (confirm == JOptionPane.YES_OPTION) {
                boolean daXoaCT = ctpndao.xoaChiTietTheoMaPN(maPN);
                boolean daXoaPN = pndao.xoaPhieuNhap(maPN);

                if (daXoaCT && daXoaPN) {
                    JOptionPane.showMessageDialog(this, "Đã hủy phiếu nhập và xóa dữ liệu thành công!");
                    loadBangCTPN();
                } else {
                    JOptionPane.showMessageDialog(this, "Lỗi khi xóa phiếu nhập hoặc chi tiết!");
                }
            }
            return;
        }

        //Trường hợp đã duyệt
        if (trangThai.equals("Đã duyệt")) {
            if (pndao.daDuyet(maPN)) {
                JOptionPane.showMessageDialog(this, "Phiếu đã duyệt trước đó!");
                return;
            }

            if (pndao.updateTrangThai(maPN, trangThai)) {
                List<ChiTietPhieuNhap> dsCT = ctpndao.getByMaPN(maPN);
                for (ChiTietPhieuNhap ct : dsCT) {
                    Double soLuongNhap = (Double) ct.getSoLuong();
                    String dvt = chuyenDonViTinh(ct.getMaNL());
                    Double soLuongQuyDoi = pndao.quyDoiDonVi(soLuongNhap, dvt);
                    pndao.capNhatSoLuongTon(ct.getMaNL(), soLuongQuyDoi);
                }
                JOptionPane.showMessageDialog(this, "Duyệt phiếu và cập nhật tồn kho thành công!");
            } else {
                JOptionPane.showMessageDialog(this, "Lỗi khi duyệt phiếu!");
            }
            return;
        }

        // Trường hợp chuyển sang "Chờ duyệt" hoặc khác
        if (pndao.updateTrangThai(maPN, trangThai)) {
            JOptionPane.showMessageDialog(this, "Đã cập nhật trạng thái!");
        } else {
            JOptionPane.showMessageDialog(this, "Lỗi khi cập nhật trạng thái!");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

