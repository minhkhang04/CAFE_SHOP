    private void btnXoaPhieuNhapActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaPhieuNhapActionPerformed
       int selectedRow = tblCTPN.getSelectedRow();
        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(this, "Vui lòng chọn một dòng để xóa!");
            return;
        }

        String maPN = cbMaPN.getSelectedItem().toString();
        String maNL = tblCTPN.getValueAt(selectedRow, 0).toString(); // Cột 0 là MaNL

        int confirm = JOptionPane.showConfirmDialog(this,
                "Bạn có chắc chắn muốn xóa chi tiết phiếu nhập với Mã NL: " + maNL + " không?",
                "Xác nhận xóa", JOptionPane.YES_NO_OPTION);

        if (confirm == JOptionPane.YES_OPTION) {
            try {
                boolean daXoa = ctpndao.xoaChiTietTheoMaPNVaMaNL(maPN, maNL); // Giả sử có phương thức này
                if (daXoa) {
                    JOptionPane.showMessageDialog(this, "Xóa chi tiết phiếu nhập thành công!");
                    loadBangCTPN(); // Tải lại bảng để cập nhật
                } else {
                    JOptionPane.showMessageDialog(this, "Lỗi khi xóa chi tiết phiếu nhập!");
                }
            } catch (SQLException e) {
                JOptionPane.showMessageDialog(this, "Lỗi khi xóa: " + e.getMessage());
            }
        }
    }//GEN-LAST:event_btnXoaPhieuNhapActionPerformed

