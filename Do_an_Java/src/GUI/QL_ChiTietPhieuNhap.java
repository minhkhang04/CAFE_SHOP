    private void cbMaPNActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cbMaPNActionPerformed
        try {
            String selectedMaPN = (String) cbMaPN.getSelectedItem();
            for (PhieuNhap pn : pndao.getAll()) {
                if (pn.getMaPN().equals(selectedMaPN)) {
                    // Format ngày nhập
                    SimpleDateFormat sdf = new SimpleDateFormat("dd/MM/yyyy");
                    String ngayNhapStr = sdf.format(pn.getNgayNhap());
                    txtNgayNhap.setText(ngayNhapStr);

                    // Format tổng tiền
                    DecimalFormat df = new DecimalFormat("#,###.##");
                    txtTongTien.setText(df.format(pn.getTongTien()));

                    break;
                }
            }
            loadBangCTPN();
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "Lỗi khi tải dữ liệu: " + e.toString());
        }
    }//GEN-LAST:event_cbMaPNActionPerformed

