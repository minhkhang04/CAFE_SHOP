    private void tableSPMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableSPMouseClicked
        int row = tableSP.getSelectedRow();
        if (row >= 0) {
            txtMaSP.setText(tableSP.getValueAt(row, 0).toString());
            txtTenSP.setText(tableSP.getValueAt(row, 1).toString());
            cbLoaiSP.setSelectedItem(tableSP.getValueAt(row, 2).toString());
            txtDonGia.setText(tableSP.getValueAt(row, 3).toString().replace(",", ""));
            txtTrangThai.setText(tableSP.getValueAt(row, 4).toString());
        }
    }//GEN-LAST:event_tableSPMouseClicked

