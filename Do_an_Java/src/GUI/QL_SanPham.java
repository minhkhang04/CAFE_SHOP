    private void btnThemSPActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemSPActionPerformed
        try {
            String maSP = txtMaSP.getText();
            String tenSP = txtTenSP.getText();
            String maLoaiSP = cbLoaiSP.getSelectedItem().toString();

            // Kiểm tra giá trị donGia
            String donGiaStr = txtDonGia.getText().trim();
            double donGia;
            try {
                donGia = Double.parseDouble(donGiaStr);
                if (donGia <= 0) {
                    JOptionPane.showMessageDialog(this, "Đơn giá phải là số dương.");
                    txtDonGia.requestFocus();
                    return;
                }
            } catch (NumberFormatException e) {
                JOptionPane.showMessageDialog(this, "Đơn giá phải là số hợp lệ.");
                txtDonGia.requestFocus();
                return;
            }

            String trangThai = txtTrangThai.getText();

            SanPham sp = new SanPham(maSP, tenSP, maLoaiSP, donGia, trangThai, "");
            if (dao.themSanPham(sp)) {
                JOptionPane.showMessageDialog(this, "Thêm sản phẩm thành công");
                hienThiDanhSachSanPham();
            } else {
                JOptionPane.showMessageDialog(this, "Thêm sản phẩm thất bại");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Lỗi: " + e.getMessage());
        }
    }//GEN-LAST:event_btnThemSPActionPerformed

