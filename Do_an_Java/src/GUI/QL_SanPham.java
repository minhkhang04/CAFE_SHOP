    private void btnTimSPActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTimSPActionPerformed
        String tuKhoa = txtTimSP.getText();
        List<SanPham> list = dao.timKiemSanPham(tuKhoa);
        DefaultTableModel model = (DefaultTableModel) tableSP.getModel();
        model.setRowCount(0);
        if (list.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Không tìm thấy sản phẩm phù hợp");
            hienThiDanhSachSanPham();
        } else {
            for (SanPham sp : list) {
                model.addRow(new Object[]{
                    sp.getMaSP(), sp.getTenSP(), sp.getMaLoaiSP(),
                    df.format(sp.getDonGia()), sp.getTrangThai()
                });
            }
        }
        
    }//GEN-LAST:event_btnTimSPActionPerformed

    private void btnTimLoaiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTimLoaiActionPerformed
        String tuKhoa = txtTimLoai.getText();
        List<LoaiSanPham> list = LoaiSanPhamDAO.timKiemLoaiSP(tuKhoa);
        DefaultTableModel model = (DefaultTableModel) tableLoaiSP.getModel();
        model.setRowCount(0);
        if (list.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Không tìm thấy loại sản phẩm phù hợp");
            hienThiDanhSachLoaiSanPham();
        } else {
            for (LoaiSanPham lsp : list) {
                model.addRow(new Object[]{lsp.getMaLoaiSP(), lsp.getTenLoai()});
            }
        }
        
    }//GEN-LAST:event_btnTimLoaiActionPerformed

