    private void hienThiDanhSachSanPham() {
        List<SanPham> list = dao.layAllDanhSachSanPham();
        DefaultTableModel model = (DefaultTableModel) tableSP.getModel();
        model.setRowCount(0);
        for (SanPham sp : list) {
            model.addRow(new Object[]{
                sp.getMaSP(), sp.getTenSP(), sp.getMaLoaiSP(),
                df.format(sp.getDonGia()), sp.getTrangThai()
            });
        }
    }
    private void hienThiDanhSachLoaiSanPham() {
        List<LoaiSanPham> list = LoaiSanPhamDAO.layDanhSachLoaiSanPham();
        DefaultTableModel model = (DefaultTableModel) tableLoaiSP.getModel();
        model.setRowCount(0);
        DefaultComboBoxModel<String> cbModel = new DefaultComboBoxModel<>();
        for (LoaiSanPham lsp : list) {
            model.addRow(new Object[]{lsp.getMaLoaiSP(), lsp.getTenLoai()});
            cbModel.addElement(lsp.getMaLoaiSP());
        }
        cbLoaiSP.setModel(cbModel);
    }
